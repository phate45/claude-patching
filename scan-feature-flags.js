#!/usr/bin/env node
/**
 * scan-feature-flags.js — Extract and diff GrowthBook feature flags from a cli.js bundle
 *
 * Detects the gate function name dynamically (it changes every build), then extracts
 * every flag call of the form GATEFN("flag_name", default). Useful for auditing what
 * flags exist in a given version and spotting additions/removals between versions.
 *
 * Usage:
 *   node scan-feature-flags.js <.pretty>                              # Print all flags
 *   node scan-feature-flags.js <.pretty> --save <inventory.json>     # Save inventory
 *   node scan-feature-flags.js <.pretty> --diff <prev-inventory.json># Diff vs previous
 *   node scan-feature-flags.js <.pretty> --save <out.json> --diff <prev.json>  # Both
 *
 * Input: the .pretty (js-beautified) file — generated by --setup.
 * Output: NDJSON — one JSON object per line, last line is the summary.
 * Inventory files (--save) are plain JSON, suitable for --diff in a later run.
 *
 * Intended integration: called during --port to generate a per-version flag snapshot
 * and automatically diff against the previous version's snapshot.
 */

'use strict';

const fs = require('fs');

// ---------------------------------------------------------------------------
// Argument parsing
// ---------------------------------------------------------------------------

const args = process.argv.slice(2);

function consumeArg(flag) {
  const idx = args.indexOf(flag);
  if (idx === -1) return null;
  const val = args[idx + 1];
  args.splice(idx, 2);
  return val ?? true;
}

const saveFile = consumeArg('--save');
const diffFile = consumeArg('--diff');
const targetPath = args.find(a => !a.startsWith('--'));

if (!targetPath) {
  process.stderr.write(JSON.stringify({ type: 'error', message: 'Usage: scan-feature-flags.js <.pretty> [--save <file>] [--diff <prev.json>]' }) + '\n');
  process.exit(1);
}

// ---------------------------------------------------------------------------
// Output helpers (inline — no dep on lib/output.js for standalone use)
// ---------------------------------------------------------------------------

function emit(obj) {
  process.stdout.write(JSON.stringify(obj) + '\n');
}

// ---------------------------------------------------------------------------
// Load source
// ---------------------------------------------------------------------------

let content;
try {
  content = fs.readFileSync(targetPath, 'utf8');
} catch (err) {
  emit({ type: 'error', message: `Cannot read ${targetPath}: ${err.message}` });
  process.exit(1);
}

// ---------------------------------------------------------------------------
// Detect gate function
//
// The GrowthBook feature gate function is the only function called as:
//   SHORTNAME("tengu_flag_name", !0|!1)   ← simple boolean scalar default
// Telemetry calls use the same tengu_ strings but always pass object literals,
// so filtering to boolean-only candidates isolates the gate function cleanly.
// ---------------------------------------------------------------------------

// Phase 1: find gate function from boolean-default calls (exclusive to the gate fn).
const BOOL_GATE_RE = /(?<![.\w])([$\w]{1,6})\("(tengu_[a-z0-9_]+)",\s*(!0|!1)\)/g;

const boolFnCounts = new Map();
for (const m of content.matchAll(BOOL_GATE_RE)) {
  const fn = m[1];
  boolFnCounts.set(fn, (boolFnCounts.get(fn) ?? 0) + 1);
}

if (boolFnCounts.size === 0) {
  emit({ type: 'error', message: 'No boolean-default feature flag calls found. Is this a valid cli.js bundle?' });
  process.exit(1);
}

const gateFunction = [...boolFnCounts.entries()].sort((a, b) => b[1] - a[1])[0][0];

emit({ type: 'gate_function', name: gateFunction, boolCallCount: boolFnCounts.get(gateFunction) });

// ---------------------------------------------------------------------------
// Extract all flag calls with line numbers
//
// Phase 2: scan line-by-line using the confirmed gate function so each match
// has a precise line number. Restricts to single-line defaults — multiline
// object literals in the pretty file are telemetry payloads, not flag defaults.
// ---------------------------------------------------------------------------

const fnEscaped = gateFunction.replace(/[$\\^.*+?()[\]{}|]/g, '\\$&');

// Non-global: applied fresh to each line via matchAll
const FLAG_LINE_RE = new RegExp(
  `(?<![.\\w])${fnEscaped}\\("(tengu_[a-z0-9_]+|quiet_[a-z0-9_]+|loud_[a-z0-9_]+)",\\s*([^)\n]+?)\\)`,
  'g'
);

// flagName → { defaultsByValue: Map<raw, lineNos[]>, lines: [] }
// Tracking distinct defaults per call site lets us detect inconsistent usage.
const flags = new Map();

const lines = content.split('\n');
for (let i = 0; i < lines.length; i++) {
  const lineNo = i + 1;
  for (const m of lines[i].matchAll(FLAG_LINE_RE)) {
    const name = m[1];
    const rawDefault = m[2].trim();
    if (!flags.has(name)) {
      flags.set(name, { defaultsByValue: new Map(), lines: [] });
    }
    const entry = flags.get(name);
    if (!entry.defaultsByValue.has(rawDefault)) entry.defaultsByValue.set(rawDefault, []);
    entry.defaultsByValue.get(rawDefault).push(lineNo);
    entry.lines.push(lineNo);
  }
}

// Derive a canonical default per flag (the value with the most call sites;
// tie-break alphabetically so it's stable). Used for diff comparison.
function canonicalDefault(entry) {
  return [...entry.defaultsByValue.entries()]
    .sort((a, b) => b[1].length - a[1].length || a[0].localeCompare(b[0]))[0][0];
}

// ---------------------------------------------------------------------------
// Classify defaults (used only for grouping the default_shapes header)
// ---------------------------------------------------------------------------

function classifyDefault(raw) {
  if (raw === '!0' || raw === '!1') return 'boolean';
  if (raw === 'null')               return 'null';
  if (raw === '""' || raw === "''") return 'empty-string';
  if (raw === '{}')                 return 'empty-object';
  if (raw === '[]')                 return 'empty-array';
  if (/^-?\d+(\.\d+)?$/.test(raw)) return 'number';
  if (/^["']/.test(raw))            return 'string';
  return 'variable'; // minified identifier — value is server-pushed
}

// ---------------------------------------------------------------------------
// Emit default_shapes header
//
// Groups non-boolean defaults by class with their unique raw values, emitted
// once at the top so readers know what e.g. "CI6" means without per-entry noise.
// ---------------------------------------------------------------------------

const shapeGroups = {};
for (const [, info] of flags) {
  for (const raw of info.defaultsByValue.keys()) {
    const cls = classifyDefault(raw);
    if (cls === 'boolean') continue; // !0 / !1 are self-explanatory
    if (!shapeGroups[cls]) shapeGroups[cls] = new Set();
    shapeGroups[cls].add(raw);
  }
}

// Emit only classes that actually appear
const shapesOut = {};
for (const [cls, vals] of Object.entries(shapeGroups)) {
  shapesOut[cls] = [...vals].sort();
}

if (Object.keys(shapesOut).length > 0) {
  emit({ type: 'default_shapes', shapes: shapesOut });
}

// ---------------------------------------------------------------------------
// Emit flags (only when not diffing — diff mode replaces this with its own output)
// ---------------------------------------------------------------------------

const sortedFlags = [...flags.entries()].sort(([a], [b]) => a.localeCompare(b));

if (!diffFile) {
  for (const [name, info] of sortedFlags) {
    const isInconsistent = info.defaultsByValue.size > 1;
    if (isInconsistent) {
      // Multiple distinct defaults across call sites — highlight the breakdown.
      emit({
        type: 'flag',
        name,
        inconsistentDefaults: [...info.defaultsByValue.entries()]
          .sort((a, b) => b[1].length - a[1].length || a[0].localeCompare(b[0]))
          .map(([def, lns]) => ({ default: def, occurrences: lns.length, lines: lns })),
        occurrences: info.lines.length,
        lines: info.lines,
      });
    } else {
      emit({
        type: 'flag',
        name,
        default: canonicalDefault(info),
        occurrences: info.lines.length,
        lines: info.lines,
      });
    }
  }
}

// ---------------------------------------------------------------------------
// Save inventory (line numbers omitted — not stable across versions)
// ---------------------------------------------------------------------------

const inventory = {
  version: 'unknown',
  scannedAt: new Date().toISOString(),
  gateFunction,
  defaultShapes: shapesOut,
  flags: Object.fromEntries(
    sortedFlags.map(([name, info]) => [name, {
      default: canonicalDefault(info),
      defaultClass: classifyDefault(canonicalDefault(info)),
      occurrences: info.lines.length,
      lines: info.lines,
    }])
  ),
};

const versionMatch = content.match(/VERSION:\s*"([^"]+)"/);
if (versionMatch) inventory.version = versionMatch[1];

if (saveFile) {
  try {
    fs.writeFileSync(saveFile, JSON.stringify(inventory, null, 2));
    emit({ type: 'saved', file: saveFile, flagCount: flags.size });
  } catch (err) {
    emit({ type: 'error', message: `Cannot write ${saveFile}: ${err.message}` });
  }
}

// ---------------------------------------------------------------------------
// Diff vs previous inventory
//
// Output shape:
//   diff_meta  — versions and counts summary
//   diff_removed — string list of flags gone in current
//   flag (type=flag) — one entry per added/changed flag in standard format,
//                      with a `change` field ("added" | "default_changed" |
//                      "occurrences_changed") and `previousDefault` /
//                      `previousOccurrences` where relevant.
// ---------------------------------------------------------------------------

if (diffFile) {
  let prev;
  try {
    prev = JSON.parse(fs.readFileSync(diffFile, 'utf8'));
  } catch (err) {
    emit({ type: 'error', message: `Cannot read diff file ${diffFile}: ${err.message}` });
    process.exit(1);
  }

  const prevFlags = prev.flags ?? {};
  const currNames = new Set(flags.keys());
  const prevNames = new Set(Object.keys(prevFlags));

  const removed = [...prevNames].filter(n => !currNames.has(n)).sort();

  // Collect added + changed flags as annotated flag entries
  const diffFlags = [];

  for (const [name, info] of sortedFlags) {
    if (!prevNames.has(name)) {
      diffFlags.push({ name, info, change: 'added' });
    } else {
      const prev_ = prevFlags[name];
      const currDef = canonicalDefault(info);
      const currClass = classifyDefault(currDef);
      const prevClass = prev_.defaultClass ?? classifyDefault(prev_.default);
      // Variable-to-variable renames are minifier noise — ignore them.
      // Only flag when the semantic class changes, or when both are the same
      // non-variable class but the value itself differs (e.g. !1 → !0).
      const classChanged = currClass !== prevClass;
      const valueChanged = currClass !== 'variable' && currDef !== prev_.default;
      if (classChanged || valueChanged) {
        diffFlags.push({ name, info, change: 'default_changed', previousDefault: prev_.default });
      } else if (info.lines.length !== prev_.occurrences) {
        diffFlags.push({ name, info, change: 'occurrences_changed', previousOccurrences: prev_.occurrences });
      }
    }
  }

  emit({
    type: 'diff_meta',
    fromVersion: prev.version ?? 'unknown',
    toVersion: inventory.version,
    addedCount: diffFlags.filter(f => f.change === 'added').length,
    removedCount: removed.length,
    changedCount: diffFlags.filter(f => f.change !== 'added').length,
  });

  if (removed.length > 0) {
    emit({ type: 'diff_removed', flags: removed });
  }

  for (const { name, info, change, previousDefault, previousOccurrences } of diffFlags) {
    const entry = {
      type: 'flag',
      name,
      default: canonicalDefault(info),
      occurrences: info.lines.length,
      lines: info.lines,
      change,
    };
    if (previousDefault !== undefined)    entry.previousDefault    = previousDefault;
    if (previousOccurrences !== undefined) entry.previousOccurrences = previousOccurrences;
    emit(entry);
  }
}

// ---------------------------------------------------------------------------
// Summary
// ---------------------------------------------------------------------------

const defaultOn  = sortedFlags.filter(([, i]) => canonicalDefault(i) === '!0').length;
const defaultOff = sortedFlags.filter(([, i]) => canonicalDefault(i) === '!1').length;
const other      = sortedFlags.length - defaultOn - defaultOff;

emit({
  type: 'summary',
  version: inventory.version,
  gateFunction,
  total: flags.size,
  defaultOn,
  defaultOff,
  otherDefault: other,
});
