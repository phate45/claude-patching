#!/usr/bin/env node
/**
 * scan-env-vars.js — Extract and diff CLAUDE_CODE_* / ANTHROPIC_* env vars from a cli.js bundle
 *
 * Sibling to scan-feature-flags.js. Where the flag scanner keys off a single gate
 * function signature (FN("tengu_…", default)), env vars have no uniform accessor —
 * they're read as process.env.X, via the minified env mirror (z$.X), as quoted
 * string keys, etc. So this scanner extracts by NAME TOKEN wherever it appears and
 * records the distinct access FORMS instead of a default value.
 *
 * Usage:
 *   node scan-env-vars.js <.pretty>                              # Print all vars
 *   node scan-env-vars.js <.pretty> --save <inventory.json>      # Save inventory
 *   node scan-env-vars.js <.pretty> --diff <prev-inventory.json> # Diff vs previous
 *   node scan-env-vars.js <.pretty> --save <out.json> --diff <prev.json>  # Both
 *
 * Input: the .pretty (js-beautified) file — generated by --setup. Minified input
 * also works; only the per-var `lines` become uninformative (everything on line 1).
 * Output: NDJSON — one JSON object per line, last line is the summary.
 * Inventory files (--save) are plain JSON, suitable for --diff in a later run.
 *
 * Intended integration: called during --port to generate a per-version env snapshot
 * and automatically diff against the previous version's snapshot.
 */

'use strict';

const fs = require('fs');

// ---------------------------------------------------------------------------
// Argument parsing (mirrors scan-feature-flags.js)
// ---------------------------------------------------------------------------

const args = process.argv.slice(2);

function consumeArg(flag) {
  const idx = args.indexOf(flag);
  if (idx === -1) return null;
  const val = args[idx + 1];
  args.splice(idx, 2);
  return val ?? true;
}

const saveFile = consumeArg('--save');
const diffFile = consumeArg('--diff');
const targetPath = args.find(a => !a.startsWith('--'));

if (!targetPath) {
  process.stderr.write(JSON.stringify({ type: 'error', message: 'Usage: scan-env-vars.js <.pretty> [--save <file>] [--diff <prev.json>]' }) + '\n');
  process.exit(1);
}

// ---------------------------------------------------------------------------
// Output helper (inline — no dep on lib/output.js for standalone use)
// ---------------------------------------------------------------------------

function emit(obj) {
  process.stdout.write(JSON.stringify(obj) + '\n');
}

// ---------------------------------------------------------------------------
// Load source
// ---------------------------------------------------------------------------

let content;
try {
  content = fs.readFileSync(targetPath, 'utf8');
} catch (err) {
  emit({ type: 'error', message: `Cannot read ${targetPath}: ${err.message}` });
  process.exit(1);
}

// ---------------------------------------------------------------------------
// Extract env var name tokens with line numbers
//
// Two prefixes by request: CLAUDE_CODE_ and ANTHROPIC_. Env names are
// conventionally upper-snake, so [A-Z0-9_]+ after the prefix is sufficient.
// We capture every occurrence regardless of access shape, then classify the
// FORM from the characters immediately preceding the match:
//   process.env  — process.env.NAME  (the canonical read)
//   property     — <id>.NAME         (e.g. the minified env mirror z$.NAME)
//   string       — "NAME" / 'NAME' / `NAME`  (key / telemetry, not a live read)
//   bracket      — [NAME]            (computed access)
//   other        — bare token in some other context
// Forms are NORMALISED (no minified identifiers baked in) so the inventory
// stays stable across builds — only name presence + occurrences drive the diff.
// ---------------------------------------------------------------------------

// CLAUDE_CODE_ leads the alternation deliberately: names like
// CLAUDE_CODE_USE_ANTHROPIC_AWS embed an ANTHROPIC_ substring. matchAll
// consumes the whole enclosing CLAUDE_CODE_ token and advances past it, so we
// never emit a phantom interior ANTHROPIC_AWS. (A bare `rg ANTHROPIC_[A-Z0-9_]+`
// over-counts for exactly this reason.)
const PREFIXES = ['CLAUDE_CODE_', 'ANTHROPIC_'];
const NAME_RE = /(CLAUDE_CODE_[A-Z0-9_]+|ANTHROPIC_[A-Z0-9_]+)/g;

function classifyForm(before) {
  if (/process\.env\.$/.test(before)) return 'process.env';
  if (/[\w$]\.$/.test(before))        return 'property';
  if (/["'`]$/.test(before))          return 'string';
  if (/\[$/.test(before))             return 'bracket';
  return 'other';
}

function prefixOf(name) {
  return PREFIXES.find(p => name.startsWith(p));
}

// name → { prefix, forms: Set, lines: [] }
const vars = new Map();

const lines = content.split('\n');
for (let i = 0; i < lines.length; i++) {
  const line = lines[i];
  const lineNo = i + 1;
  for (const m of line.matchAll(NAME_RE)) {
    const name = m[1];
    if (!vars.has(name)) {
      vars.set(name, { prefix: prefixOf(name), forms: new Set(), lines: [] });
    }
    const entry = vars.get(name);
    entry.forms.add(classifyForm(line.slice(0, m.index)));
    entry.lines.push(lineNo);
  }
}

if (vars.size === 0) {
  emit({ type: 'error', message: 'No CLAUDE_CODE_/ANTHROPIC_ env vars found. Is this a valid cli.js bundle?' });
  process.exit(1);
}

const sortedVars = [...vars.entries()].sort(([a], [b]) => a.localeCompare(b));

// ---------------------------------------------------------------------------
// Emit prefix counts header
// ---------------------------------------------------------------------------

const prefixCounts = {};
for (const p of PREFIXES) prefixCounts[p] = 0;
for (const [, info] of sortedVars) if (info.prefix) prefixCounts[info.prefix]++;

emit({ type: 'prefix_counts', prefixes: prefixCounts });

// ---------------------------------------------------------------------------
// Emit vars (only when not diffing — diff mode replaces this with its own output)
// ---------------------------------------------------------------------------

if (!diffFile) {
  for (const [name, info] of sortedVars) {
    emit({
      type: 'env_var',
      name,
      prefix: info.prefix,
      forms: [...info.forms].sort(),
      occurrences: info.lines.length,
      lines: info.lines,
    });
  }
}

// ---------------------------------------------------------------------------
// Build inventory
// ---------------------------------------------------------------------------

const inventory = {
  version: 'unknown',
  scannedAt: new Date().toISOString(),
  prefixes: PREFIXES,
  prefixCounts,
  vars: Object.fromEntries(
    sortedVars.map(([name, info]) => [name, {
      prefix: info.prefix,
      forms: [...info.forms].sort(),
      occurrences: info.lines.length,
      lines: info.lines,
    }])
  ),
};

const versionMatch = content.match(/VERSION:\s*"([^"]+)"/);
if (versionMatch) inventory.version = versionMatch[1];

if (saveFile) {
  try {
    fs.writeFileSync(saveFile, JSON.stringify(inventory, null, 2));
    emit({ type: 'saved', file: saveFile, varCount: vars.size });
  } catch (err) {
    emit({ type: 'error', message: `Cannot write ${saveFile}: ${err.message}` });
  }
}

// ---------------------------------------------------------------------------
// Diff vs previous inventory
//
// Output shape (env_ prefixed so it never collides with flag-scan output):
//   env_diff_meta — versions and counts summary
//   env_removed   — string list of vars gone in current
//   env_var (with `change`) — one entry per added/changed var, tagged
//                             "added" | "occurrences_changed". There is no
//                             default to change, so no "default_changed".
// ---------------------------------------------------------------------------

if (diffFile) {
  let prev;
  try {
    prev = JSON.parse(fs.readFileSync(diffFile, 'utf8'));
  } catch (err) {
    emit({ type: 'error', message: `Cannot read diff file ${diffFile}: ${err.message}` });
    process.exit(1);
  }

  const prevVars = prev.vars ?? {};
  const currNames = new Set(vars.keys());
  const prevNames = new Set(Object.keys(prevVars));

  const removed = [...prevNames].filter(n => !currNames.has(n)).sort();

  const diffVars = [];
  for (const [name, info] of sortedVars) {
    if (!prevNames.has(name)) {
      diffVars.push({ name, info, change: 'added' });
    } else if (info.lines.length !== prevVars[name].occurrences) {
      diffVars.push({ name, info, change: 'occurrences_changed', previousOccurrences: prevVars[name].occurrences });
    }
  }

  emit({
    type: 'env_diff_meta',
    fromVersion: prev.version ?? 'unknown',
    toVersion: inventory.version,
    addedCount: diffVars.filter(v => v.change === 'added').length,
    removedCount: removed.length,
    changedCount: diffVars.filter(v => v.change !== 'added').length,
  });

  if (removed.length > 0) {
    emit({ type: 'env_removed', vars: removed });
  }

  for (const { name, info, change, previousOccurrences } of diffVars) {
    const entry = {
      type: 'env_var',
      name,
      prefix: info.prefix,
      forms: [...info.forms].sort(),
      occurrences: info.lines.length,
      lines: info.lines,
      change,
    };
    if (previousOccurrences !== undefined) entry.previousOccurrences = previousOccurrences;
    emit(entry);
  }
}

// ---------------------------------------------------------------------------
// Summary
// ---------------------------------------------------------------------------

emit({
  type: 'summary',
  version: inventory.version,
  total: vars.size,
  prefixCounts,
});
